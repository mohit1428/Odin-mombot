import requests
import os
import glob



# Read API
HuggingFace_API = 'MY_HUGGINGFACE_API_TOKEN_HERE'	# ADD API TOKEN HERE

# Using Facebook mBART-50 large model API
API_URL = "https://api-inference.huggingface.co/models/facebook/mbart-large-50"
headers = {"Authorization": HuggingFace_API}



# Input and Output directory
input_dir = os.path.abspath('../input/')
output_dir = os.path.abspath('../output/')



# Calling Hugging face API to get response
def query(payload):
	response = requests.post(API_URL, headers=headers, json=payload)
	return response.json()

def summarize():
    if os.path.exists(input_dir) and len(os.listdir(input_dir)) > 0:
        input_file = os.path.join(input_dir, 'test.txt')

        # Open a file: file
        file = open(input_file, mode='r')
        text = file.read()

        # Sending input data for summary generation
        output = query({
            "inputs": text
        })

        # Closing file
        file.close()

        # Returining summarized result
        summary = output[0]['generated_text']
        return summary

    # In case input file is not present
    else:
        print(' > INVALID INPUT')

def output_writer(summary):
        #open text file
        output_file = open(os.path.join(output_dir, 'output.txt'), 'w')

        #write string to file
        output_file.write(summary)

        #close file
        output_file.close()



def main():
    summary = summarize()
    output_writer(summary)
    print(summary)



if __name__ == '__main__':
    main()


